
public class App {
    public static void main(String[] args) throws Exception {
        System.out.println("Hello, World!");
        // 1~10までを足して結果をコンソールに出力
        int sum = 0;
        for (int i = 1; i <= 10; i++) {
            sum += i;
        }
        System.out.println(sum);

    }
}
